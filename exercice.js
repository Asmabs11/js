function MaxMin (arr){
let max=0;
let min=0;

for (let i=0; i<arr.max;i++)
     if (arr[i] > max) {
        max = arr[i]
    }
    if (arr[i] < min) {
        min = arr[i];
    }

return maximum - minimum ;
}

